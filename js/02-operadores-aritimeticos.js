// sobre operadores atitiméticos
// // Sobre Operadores aritméticos
/* 
  Os mais comuns são:

  soma ( + )
  subtração ( - )
  Multiplicação ( * )
  Divisão ( / ) 
*/

// Exemplo de soma
let numero1 = 90;
let numero2 = 3.4;

let soma = numero1 + numero2;
console.log(soma);

// Exemplo de subtração
let subtracao = numero1 - numero2;
console.log(subtracao);

// Exemplo de Multiplicação
let multiplicacao = numero1 * numero2;
console.log(multiplicacao);

// Exemplo de divisão
let divisao = numero1 / numero2;
console.log(divisao);
console.log(typeof divisao);

// Outros operadores aritméticos
// Exponênciação ( ** )
// Módulo ( % )

// Exemplo de exponênciação
const numero3 = 5;
const numero4 = 3;

// 5³ (cinco elevado a 3) = 5x5X5
let exponenciacao = numero3 ** numero4;
console.log(exponenciacao);

// O módulo retorna o resto da divisão entre dois números
// Exemplo de módulo
let modulo = numero3 % numero4;
console.log(modulo);